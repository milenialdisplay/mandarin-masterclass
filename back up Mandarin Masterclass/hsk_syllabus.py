def get_lesson(level, lesson_num):
    """Get lesson data for a specific level and lesson number"""
    if level in HSK_SYLLABUS and lesson_num <= len(HSK_SYLLABUS[level]["lessons"]):
        return HSK_SYLLABUS[level]["lessons"][lesson_num - 1]
    return None

def get_level_info(level):
    """Get overview information for a level"""
    return HSK_SYLLABUS.get(level, {})

def get_total_lessons(level):
    """Get total number of lessons for a level"""
    return HSK_SYLLABUS.get(level, {}).get('total_lessons', 0)

def get_all_levels_summary():
    """Get summary of all HSK levels"""
    summary = {}
    for level in range(1, 7):
        if level in HSK_SYLLABUS:
            summary[level] = {
                "name": HSK_SYLLABUS[level]["name"],
                "total_lessons": HSK_SYLLABUS[level]["total_lessons"],
                "total_words": HSK_SYLLABUS[level]["total_words"],
                "description": HSK_SYLLABUS[level]["description"]
            }
    return summary